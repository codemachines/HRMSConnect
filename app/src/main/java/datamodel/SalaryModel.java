package datamodel;

public class SalaryModel {

    String sdate;
    String twd;
    String datt;
    String bs;
    String tax;
    String allowance;
    String total_salary;

    public String getSdate() {
        return sdate;
    }

    public void setSdate(String sdate) {
        this.sdate = sdate;
    }

    public String getTwd() {
        return twd;
    }

    public void setTwd(String twd) {
        this.twd = twd;
    }

    public String getDatt() {
        return datt;
    }

    public void setDatt(String datt) {
        this.datt = datt;
    }

    public String getBs() {
        return bs;
    }

    public void setBs(String bs) {
        this.bs = bs;
    }

    public String getTax() {
        return tax;
    }

    public void setTax(String tax) {
        this.tax = tax;
    }

    public String getAllowance() {
        return allowance;
    }

    public void setAllowance(String allowance) {
        this.allowance = allowance;
    }

    public String getTotal_salary() {
        return total_salary;
    }

    public void setTotal_salary(String total_salary) {
        this.total_salary = total_salary;
    }
}
